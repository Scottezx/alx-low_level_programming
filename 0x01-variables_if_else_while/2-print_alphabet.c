#include <stdio.h>

/**
 * main - Prints the alphabet in lowercase
 *
 * Return: Always 0 
 */
int main()
{
        char c;

        c = 'a';
        while
                (c <= 'z') {
                        putchar(c);
                        c++;
                }
        putchar('\n');
        return (0);
}
